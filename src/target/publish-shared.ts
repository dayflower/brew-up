import Mustache from "mustache";
import type { ValidatedInputs } from "../types.js";

interface CommitIdentity {
  name: string;
  email: string;
}

interface CreateOrUpdateFileRequest {
  owner: string;
  repo: string;
  path: string;
  branch: string;
  message: string;
  content: string;
  sha?: string;
  committer?: CommitIdentity;
  author?: CommitIdentity;
}

type PublishConfig = Pick<
  ValidatedInputs,
  | "outputPath"
  | "targetRepo"
  | "commitAuthor"
  | "publishTitleTemplate"
  | "publishBodyTemplate"
  | "publishAttribution"
>;

export interface PublishTemplateVariables {
  output_path: string;
  version: string;
  tag_name: string;
  release_id: string;
  release_name: string;
  release_url: string;
}

interface PublishRenderedMessage {
  title: string;
  body: string;
}

const PLACEHOLDER_PATTERN = /\{\{\{?\s*([a-zA-Z0-9_.]+)\s*\}\}?\}/g;
const BREW_UP_URL = "https://github.com/dayflower/brew-up";
const COMMIT_ATTRIBUTION_LINE = `Generated by brew-up: ${BREW_UP_URL}`;
const PR_ATTRIBUTION_LINE = `Generated by [brew-up](${BREW_UP_URL})`;

function hasPath(target: unknown, path: string): boolean {
  const keys = path.split(".");
  let current: unknown = target;

  for (const key of keys) {
    if (
      current === null ||
      current === undefined ||
      typeof current !== "object" ||
      !(key in current)
    ) {
      return false;
    }

    current = (current as Record<string, unknown>)[key];
  }

  return true;
}

function replaceMissingPlaceholdersWithUnknown(
  template: string,
  context: PublishTemplateVariables,
): string {
  return template.replaceAll(PLACEHOLDER_PATTERN, (fullMatch, variableName) => {
    return hasPath(context, variableName) ? fullMatch : "UNKNOWN";
  });
}

function renderPublishTemplate(
  template: string,
  variables: PublishTemplateVariables,
): string {
  const resolvedTemplate = replaceMissingPlaceholdersWithUnknown(
    template,
    variables,
  );
  return Mustache.render(resolvedTemplate, variables, undefined, {
    escape: (value) => String(value),
  });
}

function appendAttribution(input: {
  body: string;
  attributionEnabled: boolean;
  attributionLine: string;
}): string {
  const body = input.body.trim();
  if (!input.attributionEnabled) {
    return body;
  }
  if (body === "") {
    return input.attributionLine;
  }
  return `${body}\n\n${input.attributionLine}`;
}

export function buildRenderedPublishMessage(
  config: Pick<
    ValidatedInputs,
    "publishTitleTemplate" | "publishBodyTemplate" | "publishAttribution"
  >,
  variables: PublishTemplateVariables,
): PublishRenderedMessage {
  const title = renderPublishTemplate(config.publishTitleTemplate, variables);
  const bodyTemplate = renderPublishTemplate(
    config.publishBodyTemplate,
    variables,
  );

  const body = appendAttribution({
    body: bodyTemplate,
    attributionEnabled:
      config.publishAttribution === "both" ||
      config.publishAttribution === "commit",
    attributionLine: COMMIT_ATTRIBUTION_LINE,
  });

  return {
    title,
    body,
  };
}

export function buildPullRequestBody(
  config: Pick<ValidatedInputs, "publishBodyTemplate" | "publishAttribution">,
  variables: PublishTemplateVariables,
): string | undefined {
  const bodyTemplate = renderPublishTemplate(
    config.publishBodyTemplate,
    variables,
  );
  const body = appendAttribution({
    body: bodyTemplate,
    attributionEnabled:
      config.publishAttribution === "both" ||
      config.publishAttribution === "pr",
    attributionLine: PR_ATTRIBUTION_LINE,
  });
  return body === "" ? undefined : body;
}

export function buildFileWriteRequest(
  config: PublishConfig,
  branch: string,
  renderedOutput: string,
  options: { currentSha?: string; messageVariables: PublishTemplateVariables },
): CreateOrUpdateFileRequest {
  const renderedMessage = buildRenderedPublishMessage(
    config,
    options.messageVariables,
  );
  const commitMessage =
    renderedMessage.body === ""
      ? renderedMessage.title
      : `${renderedMessage.title}\n\n${renderedMessage.body}`;

  return {
    owner: config.targetRepo.owner,
    repo: config.targetRepo.name,
    path: config.outputPath,
    branch,
    message: commitMessage,
    content: Buffer.from(renderedOutput, "utf8").toString("base64"),
    sha: options.currentSha,
    committer: config.commitAuthor,
    author: config.commitAuthor,
  };
}
